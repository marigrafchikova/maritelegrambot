@dp.message_handler(text=user_message)
async def user_mode(message: types.Message):
    markup_menu = ReplyKeyboardMarkup(resize_keyboard=True)
    markup_menu.insert(menu_message)
    cid = message.chat.id
    if cid in ADMINS:
        ADMINS.remove(cid)


    # await message.answer('', reply_markup=ReplyKeyboardRemove())
    await message.answer('–í–∫–ª—é—á–µ–Ω –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π —Ä–µ–∂–∏–º.', reply_markup=markup_menu)


@dp.message_handler(text=admin_message)
async def admin_mode(message: types.Message):
    markup_menu = ReplyKeyboardMarkup(resize_keyboard=True)
    markup_menu.insert(menu_message)
    cid = message.chat.id
    if cid not in ADMINS:
        ADMINS.append(cid)


    # await message.answer('', reply_markup=ReplyKeyboardRemove())
    await message.answer('–í–∫–ª—é—á–µ–Ω –∞–¥–º–∏–Ω—Å–∫–∏–π —Ä–µ–∂–∏–º.', reply_markup=markup_menu)
    

async def on_startup(dp):
    logging.basicConfig(level=logging.INFO)
    db.create_tables()

    await bot.delete_webhook()
    await bot.set_webhook(WEBHOOK_URL)


async def on_shutdown():
    logging.warning("Shutting down..")
    await bot.delete_webhook()
    await dp.storage.close()
    await dp.storage.wait_closed()
    logging.warning("Bot down")


if __name__ == '__main__':

    if "HEROKU" in list(os.environ.keys()):

        executor.start_webhook(
            dispatcher=dp,
            webhook_path=WEBHOOK_PATH,
            on_startup=on_startup,
            on_shutdown=on_shutdown,
            skip_updates=True,
            host=WEBAPP_HOST,
            port=WEBAPP_PORT,
        )

    else:

        executor.start_polling(dp, on_startup=on_startup, skip_updates=False)
